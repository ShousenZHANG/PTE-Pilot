const MIN_TRAINABLE_LENGTH = 4;

export function isTrainableWord(expected: string): boolean {
  const word = expected.trim().toLocaleLowerCase("en-AU");
  if (word.length < MIN_TRAINABLE_LENGTH) return false;
  return !STOP_WORDS.has(word);
}
